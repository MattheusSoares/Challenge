import { AppRoutingModule } from './app-routing.module';
import { AppComponent } from './app.component';
import { HeaderComponent } from './views/layout/header/header.component';
import { PageLoaderComponent } from './views/layout/page-loader/page-loader.component';
import { SidebarComponent } from './views/layout/sidebar/sidebar.component';
import { RightSidebarComponent } from './views/layout/right-sidebar/right-sidebar.component';
import { AuthLayoutComponent } from './views/layout/app-layout/auth-layout/auth-layout.component';
import { MainLayoutComponent } from './views/layout/app-layout/main-layout/main-layout.component';
import { fakeBackendProvider } from './core/interceptor/fake-backend';
